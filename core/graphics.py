class Graphics:
    
    @staticmethod
    def drawText(grid, x: int, y: int, text: str, color: str, font=FONT_3X5):
        cursorX = x
        for char in text:
            charKey = char.upper()
            if charKey in font:
                bitmap = font[charKey]
            elif char in font: # Case sensitive fallback
                bitmap = font[char]
            else:
                 # Default to space if not found or try mapped
                if '?' in font: bitmap = font['?']
                else: bitmap = font.get(' ', [[0]])

            height = len(bitmap)
            width = len(bitmap[0])
            
            for r in range(height):
                for c in range(width):
                    if r < len(bitmap) and c < len(bitmap[r]) and bitmap[r][c]:
                        grid.setPixel(cursorX + c, y + r, color)
            
            cursorX += width + 1 # 1px spacing

    @staticmethod
    def drawTextCentered(grid, y: int, text: str, color: str, font=FONT_3X5):
        # Calculate width
        totalWidth = 0
        for char in text:
            charKey = char.upper()
            bitmap = font.get(charKey, font.get(' ', [[0]]))
            totalWidth += len(bitmap[0]) + 1
        totalWidth -= 1 # Remove last space
        
        startX = (grid.width - totalWidth) // 2
        Graphics.drawText(grid, startX, y, text, color, font)

